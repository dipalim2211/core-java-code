package com.example;

public class ArrayDataTypeDemo {

    public static void main(String[] args) {

        int[] arr = {10,20,30,40,50};
        float[] fArray ={10.5f,20.5f,30.5f,40.5f};
        String[] s1= {"Dipa","Snowy","Mahesh","Chinmay"};

        System.out.println(arr);

        System.out.println("Print Using Index");
        System.out.println(arr[0]);
        System.out.println(arr[1]);

        System.out.println("Print Float array");
        System.out.println(fArray[1]);

        System.out.println("Print String array");
        System.out.println(s1[1]);

    }


}
