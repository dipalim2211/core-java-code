package com.example;

public class VariableDeclarationDemo {

    static int i;       //calling method is Static
    static float f;
    static double d;
    static boolean b;
    static long l;

    public static void main(String[] args) {

        System.out.println(i); // called static variable
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(l);


    }
}
